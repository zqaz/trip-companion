export function searchCities(query: string): City[] {
  if (query.length < 2) return [];
  const q = query.toLowerCase();
  return CITIES.filter(
    c => c.city.toLowerCase().includes(q) || c.country.toLowerCase().includes(q)
  ).slice(0, 8);
}

export function searchCitiesForCountry(country: string, query: string): City[] {
  const normalizedCountry = country.toLowerCase();
  const inCountry = CITIES.filter(c => c.country.toLowerCase() === normalizedCountry);
  if (!query || query.length < 1) return inCountry.slice(0, 10);
  const q = query.toLowerCase();
  return inCountry.filter(c => c.city.toLowerCase().includes(q)).slice(0, 10);
}

export function cityLabel(city: City): string {
  return `${city.city}, ${city.country}`;
}
